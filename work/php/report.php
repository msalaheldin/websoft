    <br>- Injection: such as an SQL injection, it occurs when an attacker sends untrusted data to the system without a proper authorization. Such a thing could lead to loss of data.
    <br>- Security misconfiguration: it refers to the improper implementation of controls that intended to keep application safe. Such ass error messages containing sensitive information, not patching or upgrading system frameworks and components.
    <br>- Using components with known vulnerabilities: it is when a developer doesn’t know which open-source third party to use and either it is trusted or not on the application. Untrusted components could be exploited by attackers to take over the server and use it to steal all the sensitive data. 
    <br>- Insecure deserialization: it can enable the attacker to execute code in the application remotely, such as deleting objects or conduct injection attacks.
